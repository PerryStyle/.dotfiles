local opt = vim.opt

-- Line number
opt.number = true
opt.relativenumber = true

-- Status information
opt.showmode = false
opt.showcmd = false

-- Tab rules
opt.expandtab = true

-- Colorscheme
opt.termguicolors = true
opt.background = "dark"
opt.signcolumn = "yes"

-- Scroll and line break
opt.scrolloff = 999
opt.wrap = true
opt.linebreak = true

-- Persistent undo
opt.undofile = true

-- Color column
opt.colorcolumn = "80"

-- No swap file
opt.swapfile = false
